package com.StoreHub.StoreHub.pos.system.service.impl;

import com.StoreHub.StoreHub.pos.system.model.Customer;
import com.StoreHub.StoreHub.pos.system.repository.CustomerRepository;
import com.StoreHub.StoreHub.pos.system.service.CustomerService;
import lombok.RequiredArgsConstructor;
import org.springframework.stereotype.Service;

import java.util.List;

@Service
@RequiredArgsConstructor
public class CustomerServiceImpl implements CustomerService {

    private final CustomerRepository customerRepository;

    @Override
    public Customer createCustomer(Customer customer) {
        return customerRepository.save(customer);
    }

    @Override
    public Customer updateCustomer(Customer customer, Long id) throws Exception {
        Customer existingCustomer = customerRepository.findById(id).orElseThrow(
                () -> new Exception("Customer not found")
        );

        existingCustomer.setFullName(customer.getFullName());
        existingCustomer.setEmail(customer.getEmail());
        existingCustomer.setPhone(customer.getPhone());

        return customerRepository.save(existingCustomer);

    }

    @Override
    public void deleteCustomer(Long id) throws Exception {

        Customer customerToUpdate = customerRepository.findById(id).orElseThrow(
                ()->new Exception("Customer not found")
        );

        customerRepository.delete(customerToUpdate);
    }

    @Override
    public Customer getCustomer(Long id) throws Exception {
        return customerRepository.findById(id).orElseThrow(
                ()->new Exception("Customer not found")
        );
    }

    @Override
    public List<Customer> getAllCustomer() throws Exception {
        return customerRepository.findAll();
    }

    @Override
    public List<Customer> searchCustomers(String keyword) throws Exception {
        return customerRepository.findByFullNameContainingIgnoreCaseOrEmailContainingIgnoreCase(
                keyword,keyword
        );
    }
}
